#include <libft.h>

char	*ft_s_cat(char *s1, char *s2)
{
	int i;
	int	j;

	i = ft_s_len(s1);
	j = 0;
	while (s2[j])
	{
		s1[i++] = s2[j];
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
